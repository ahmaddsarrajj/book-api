import mongoose from "mongoose";

const orderSchema = mongoose.Schema(
  {
    customer: {
      fname:{
        type : String,
        required : true,
        autoIndexId : true
        },
      lname:{
        type: String,
        required: true,
        autoIndexId: true
        },
      phone:{
          type: Number,
          required: true,
          size: 13
        }
      
    },
    
    shippingAddress: {
      address: { type: String, required: true },
      city: { type: String, required: true },
      country: { type: String, required: true },
    },
    paymentMethod: {
      type: String,
      required: true,
      default: "Credit Card",
    },
    shippingPrice: {
      type: Number,
      required: true,
      default: 0.0,
    },
    totalPrice: {
      type: Number,
      required: true,
      default: 0.0,
    },
    isPaid: {
      type: Boolean,
      required: true,
      default: false,
    },
    paidAt: {
      type: Date,
    },

    isDelivered: {
      type: Boolean,
      required: true,
      default: false,
    },
    deliveredAt: {
      type: Date,
    },
  },
  {
    timestamps: true,
  }
);

const Order = mongoose.model("Order", orderSchema);

export default Order;
